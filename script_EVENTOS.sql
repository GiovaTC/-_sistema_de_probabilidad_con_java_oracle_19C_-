-- tabla .
CREATE TABLE EVENTOS (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    NOMBRE_EVENTO VARCHAR2(100),
    FRECUENCIA NUMBER,
    PROBABILIDAD NUMBER
);

-- Procedimiento almacenado (cálculo de probabilidad)
CREATE OR REPLACE PROCEDURE SP_CALCULAR_PROBABILIDAD AS
    V_TOTAL NUMBER;
BEGIN
    -- Total de frecuencias
    SELECT SUM(FRECUENCIA) INTO V_TOTAL FROM EVENTOS;

    -- Actualizar probabilidades
    UPDATE EVENTOS
    SET PROBABILIDAD = FRECUENCIA / V_TOTAL;

    COMMIT;
END;
/

-- Procedimiento CRUD (Insertar)
CREATE OR REPLACE PROCEDURE SP_INSERT_EVENTO(
    P_NOMBRE IN VARCHAR2,
    P_FRECUENCIA IN NUMBER
)
AS
BEGIN
    INSERT INTO EVENTOS (NOMBRE_EVENTO, FRECUENCIA)
    VALUES (P_NOMBRE, P_FRECUENCIA);

    COMMIT;
END;
/

COMMIT;